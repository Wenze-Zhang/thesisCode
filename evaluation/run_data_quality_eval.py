def _load_cases(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        raise SystemExit(
            f"Labelled dataset not found: {path}\n"
            "Run: python evaluation/generate_labelled_dataset.py"
        )
    cases = []
    with path.open("r", encoding="utf-8") as fh:
        for line_no, line in enumerate(fh, start=1):
            if not line.strip():
                continue
            try:
                cases.append(json.loads(line))
            except json.JSONDecodeError as exc:
                raise SystemExit(f"Invalid JSONL at {path}:{line_no}: {exc}") from exc
    return cases


def _matches_expected(case: dict[str, Any], actual_topic: str, payload: dict[str, Any]) -> tuple[bool, dict[str, Any]]:
    actual_quality = payload.get("quality")
    actual_error = payload.get("error")
    actual_canonicalized = bool(payload.get("canonicalized"))
    expected_error = case.get("expected_error_contains")

    error_ok = (
        actual_error in (None, "")
        if expected_error is None
        else expected_error in str(actual_error)
    )

    passed = (
        actual_topic == case["expected_topic"]
        and actual_quality == case.get("expected_quality")
        and error_ok
        and actual_canonicalized == bool(case.get("expected_canonicalized"))
    )

    row = {
        "case_id": case["case_id"],
        "category": case["category"],
        "expected_topic": case["expected_topic"],
        "actual_topic": actual_topic,
        "expected_quality": case.get("expected_quality"),
        "actual_quality": actual_quality,
        "expected_error_contains": expected_error,
        "actual_error": actual_error,
        "expected_canonicalized": bool(case.get("expected_canonicalized")),
        "actual_canonicalized": actual_canonicalized,
        "pass": passed,
    }
    return passed, row


def _rate(numerator: int, denominator: int) -> float | None:
    if denominator == 0:
        return None
    return round(numerator / denominator, 6)


def _summary(rows: list[dict[str, Any]]) -> dict[str, Any]:
    total = len(rows)
    pass_count = sum(1 for row in rows if row["pass"])
    valid_rows = [row for row in rows if row["expected_topic"] == config.KAFKA_TOPIC_TELEMETRY_VALIDATED]
    invalid_rows = [row for row in rows if row["expected_topic"] == config.KAFKA_TOPIC_DLQ]

    category_breakdown: dict[str, dict[str, Any]] = {}
    grouped: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for row in rows:
        grouped[row["category"]].append(row)
    for category, category_rows in sorted(grouped.items()):
        category_pass = sum(1 for row in category_rows if row["pass"])
        category_breakdown[category] = {
            "total": len(category_rows),
            "pass_count": category_pass,
            "fail_count": len(category_rows) - category_pass,
            "pass_rate": _rate(category_pass, len(category_rows)),
            "validated_count": sum(
                1 for row in category_rows if row["actual_topic"] == config.KAFKA_TOPIC_TELEMETRY_VALIDATED
            ),
            "dlq_count": sum(1 for row in category_rows if row["actual_topic"] == config.KAFKA_TOPIC_DLQ),
        }

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "total_cases": total,
        "pass_count": pass_count,
        "fail_count": total - pass_count,
        "valid_acceptance_rate": _rate(
            sum(1 for row in valid_rows if row["actual_topic"] == config.KAFKA_TOPIC_TELEMETRY_VALIDATED),
            len(valid_rows),
        ),
        "invalid_rejection_rate": _rate(
            sum(1 for row in invalid_rows if row["actual_topic"] == config.KAFKA_TOPIC_DLQ),
            len(invalid_rows),
        ),
        "canonicalization_accuracy": _rate(
            sum(1 for row in rows if row["actual_canonicalized"] == row["expected_canonicalized"]),
            total,
        ),
        "unexpected_dlq_rate": _rate(
            sum(1 for row in valid_rows if row["actual_topic"] == config.KAFKA_TOPIC_DLQ),
            len(valid_rows),
        ),
        "unexpected_acceptance_rate": _rate(
            sum(1 for row in invalid_rows if row["actual_topic"] == config.KAFKA_TOPIC_TELEMETRY_VALIDATED),
            len(invalid_rows),
        ),
        "category_breakdown": category_breakdown,
    }


def _write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def _write_before_after(path: Path) -> None:
    _write_csv(
        path,
        ["dimension", "before_raw_telemetry", "after_fair_bridge_output", "evidence"],
        BEFORE_AFTER_ROWS,
    )


def run(input_path: Path, results_dir: Path) -> dict[str, Any]:
    cases = _load_cases(input_path)
    reg = registry.load_registry(str(REGISTRY_PATH))
    rows = []

    for case in cases:
        actual_topic, payload = etl.process_message(case["input"], {}, reg=reg)
        _, row = _matches_expected(case, actual_topic, payload)
        rows.append(row)

    results_dir.mkdir(parents=True, exist_ok=True)
    results_csv = results_dir / "data_quality_results.csv"
    summary_json = results_dir / "data_quality_summary.json"
    before_after_csv = results_dir / "before_after_quality_comparison.csv"

    _write_csv(results_csv, RESULT_COLUMNS, rows)
    summary = _summary(rows)
    with summary_json.open("w", encoding="utf-8") as fh:
        json.dump(summary, fh, indent=2, sort_keys=True)
        fh.write("\n")
    _write_before_after(before_after_csv)

    print(
        "Data quality evaluation: "
        f"{summary['pass_count']}/{summary['total_cases']} passed; "
        f"valid_acceptance_rate={summary['valid_acceptance_rate']}; "
        f"invalid_rejection_rate={summary['invalid_rejection_rate']}"
    )
    print(f"Wrote {results_csv}")
    print(f"Wrote {summary_json}")
    print(f"Wrote {before_after_csv}")
    return summary


def main() -> int:
    parser = argparse.ArgumentParser(description="Run labelled data quality evaluation.")
    parser.add_argument("--input", type=Path, default=DEFAULT_INPUT, help="Labelled JSONL input.")
    parser.add_argument("--results-dir", type=Path, default=DEFAULT_RESULTS_DIR, help="Directory for result files.")
    args = parser.parse_args()

    summary = run(args.input, args.results_dir)
    return 0 if summary["fail_count"] == 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
